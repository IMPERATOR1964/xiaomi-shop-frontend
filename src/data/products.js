// Извлечение всех уникальных значений для каждого ключа фильтра
export const buildFilterOptions = (products, category) => {
  const conf = FILTER_CONFIG[category] || [];
  return conf.map(({ key, label }) => {
    const values = [...new Set(
      products.filter(p => p.specs?.[key] != null).map(p => String(p.specs[key]))
    )].sort((a, b) => a.localeCompare(b, 'ru'));
    return { key, label, values };
  }).filter(x => x.values.length > 1);
};

// Проверка соответствия товара фильтру
export const matchFilters = (product, filters) => {
  for (const [key, set] of Object.entries(filters)) {
    if (!set || set.size === 0) continue;
    const v = product.specs?.[key];
    if (v == null || !set.has(String(v))) return false;
  }
  return true;
};

// Поиск
export const searchProducts = (products, query) => {
  if (!query || !query.trim()) return products;
  const q = query.toLowerCase().trim();
  return products.filter(p => {
    if (p.name.toLowerCase().includes(q)) return true;
    if (p.shortDesc?.toLowerCase().includes(q)) return true;
    if (p.desc?.toLowerCase().includes(q)) return true;
    if (p.specs) {
      for (const v of Object.values(p.specs)) {
        if (String(v).toLowerCase().includes(q)) return true;
      }
    }
    return false;
  });
};

