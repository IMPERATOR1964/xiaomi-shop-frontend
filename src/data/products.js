// =========================================================
// Варианты товара — группировка по variant_group_id (бэк-совместимо).
// При отсутствии группы — fallback на specs.Модель.
// =========================================================
const groupKey = (p) => p?.variantGroup || p?.specs?.['Модель'] || null;

export const getProductVariants = (product) => {
  const key = groupKey(product);
  if (!key) return { variants: [product], colors: [], memories: [] };

  const variants = PRODUCTS.filter(p =>
    p.category === product.category && groupKey(p) === key
  );

  const colors = [...new Set(variants.map(v => v.specs?.['Цвет']).filter(Boolean))];
  const memories = [...new Set(
    variants
      .filter(v => v.specs?.['Объём ОЗУ'] && v.specs?.['Объём накопителя'])
      .map(v => `${v.specs['Объём ОЗУ']} / ${v.specs['Объём накопителя']}`)
  )];

  return { variants, colors, memories };
};

export const findVariantByOptions = (product, { color, memory }) => {
  const { variants } = getProductVariants(product);
  return variants.find(v => {
    const c = v.specs?.['Цвет'];
    const m = v.specs?.['Объём ОЗУ'] && v.specs?.['Объём накопителя']
      ? `${v.specs['Объём ОЗУ']} / ${v.specs['Объём накопителя']}`
      : null;
    return (!color || c === color) && (!memory || m === memory);
  });
};

// =========================================================
// Похожие товары — та же категория, исключая текущий и его group
// (логика бэка: ORDER BY ABS(price - basePrice))
// =========================================================
export const getSimilarProducts = (product, count = 4) => {
  if (!product) return [];
  const myGroup = groupKey(product);

  const same = PRODUCTS.filter(p =>
    p.id !== product.id && p.category === product.category
  );

  const sorted = same.sort((a, b) =>
    Math.abs(a.price - product.price) - Math.abs(b.price - product.price)
  );

  // Убираем дубликаты по variantGroup чтобы не показывать варианты той же модели
  const seen = new Set();
  if (myGroup) seen.add(myGroup);
  const result = [];
  for (const p of sorted) {
    const g = groupKey(p);
    if (g && seen.has(g)) continue;
    if (g) seen.add(g);
    result.push(p);
    if (result.length >= count) break;
  }
  return result;
};

